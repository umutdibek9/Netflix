    private void button_kayitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_kayitActionPerformed
        
        String kulanici_adi = text_kullanici.getText();
        String sifre = String.valueOf(text_sifre.getPassword());
        String mail = text_mail.getText();
        boolean[] arr = new boolean[10];
        int i,j;
        String dogum_tarihi=" ";
        j=0;
        for(i=0;i<10;i++){
            arr[i]=false;
        }
        arr[0] = text_aksiyon.isSelected();
        arr[1] = text_bilimkurgu.isSelected();
        arr[2] = text_romantizm.isSelected();
        arr[3] = text_dramalar.isSelected();
        arr[4] = text_cocuk.isSelected();
        arr[5] = text_belgesel.isSelected();
        arr[6] = text_komedi.isSelected();
        arr[7] = text_korku.isSelected();
        arr[8] = text_bilim.isSelected();
        arr[9] = text_gerilim.isSelected();
        for(i=0;i<10;i++){
            if(arr[i]==true){
                j++;
            }
        }
        if(kulanici_adi.equals("")==true){
            mesaj_kullanici.setVisible(true);
        };
        if(sifre.equals("")==true){
            mesaj_sifre.setVisible(true);
        };
        if(j!=3){
            mesaj_tur.setVisible(true);
        }
        boolean kullanici_bool = islemler.kullanici_kayit(kulanici_adi);
        if(kullanici_bool == true){
            mesaj_kullanici.setVisible(true);
        }
        boolean mail_bool = islemler.mail_adresi_kayit(mail);
        if(mail_bool == true || mail.equals("")==true){
            mesaj_mail.setVisible(true);
        }
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        boolean tarih_bool;
        if(text_tarih.getDate()==null){
            tarih_bool = true;
            mesaj_tarih.setVisible(true);
        }
        else{
            dogum_tarihi = sdf.format(text_tarih.getDate());
            tarih_bool = false;
        }
        if(kullanici_bool == false && mail_bool==false && tarih_bool==false && j==3
         && kulanici_adi.equals("")==false && sifre.equals("")==false && mail.equals("")==false){
            islemler.yenikayit(kulanici_adi, sifre, mail, dogum_tarihi);
            KayitSonrasiEkrani kayit_sonrasi_ekrani = new KayitSonrasiEkrani(arr);
            kayit_sonrasi_ekrani.setVisible(true);
            setVisible(false);
        } 
      
    }//GEN-LAST:event_button_kayitActionPerformed

