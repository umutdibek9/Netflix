
public class ProgramTur {
    
    private int program_id;
    private int tur_id;

    public ProgramTur() {   
    }
    public ProgramTur(int program_id, int tur_id) {
        this.program_id = program_id;
        this.tur_id = tur_id;
    }

    public int getProgram_id() {
        return program_id;
    }

    public void setProgram_id(int program_id) {
        this.program_id = program_id;
    }

    public int getTur_id() {
        return tur_id;
    }

    public void setTur_id(int tur_id) {
        this.tur_id = tur_id;
    }
    
    
    
    
    
}
